/** Locales that render right-to-left. Used to set <html dir>. */
export const RTL_LOCALES: ReadonlySet<SupportedLocale> = new Set(["ar"]);

/** Text direction for a locale: "rtl" for Arabic, "ltr" otherwise. */
export function localeDir(locale: SupportedLocale): "rtl" | "ltr" {
  return RTL_LOCALES.has(locale) ? "rtl" : "ltr";
}

export const LOCALE_COOKIE = "dralvo-locale";
